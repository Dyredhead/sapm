    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="sapm"
                ;;
            sapm,add)
                cmd="sapm__install"
                ;;
            sapm,find)
                cmd="sapm__find"
                ;;
            sapm,help)
                cmd="sapm__help"
                ;;
            sapm,info)
                cmd="sapm__info"
                ;;
            sapm,install)
                cmd="sapm__install"
                ;;
