                cmd="sapm__list"
                ;;
            sapm,remove)
                cmd="sapm__uninstall"
                ;;
            sapm,search)
                cmd="sapm__find"
                ;;
            sapm,show)
                cmd="sapm__info"
                ;;
            sapm,uninstall)
                cmd="sapm__uninstall"
                ;;
            sapm,update)
                cmd="sapm__update"
                ;;
            sapm,upgrade)
                cmd="sapm__update"
                ;;
            sapm__help,find)
                cmd="sapm__help__find"
                ;;
            sapm__help,help)
                cmd="sapm__help__help"
                ;;
            sapm__help,info)
                cmd="sapm__help__info"
                ;;
            sapm__help,install)
                cmd="sapm__help__install"
                ;;
            sapm__help,list)
                cmd="sapm__help__list"
                ;;
            sapm__help,uninstall)
                cmd="sapm__help__uninstall"
                ;;
            sapm__help,update)
                cmd="sapm__help__update"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        sapm)
            opts="-v -h -V --pm --package-manager --verbose --help --version find search info show install add list ls uninstall remove update upgrade help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --package-manager)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                --pm)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sapm__find)
            opts="-h --help <PACKAGE>"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sapm__find)
            opts="-h --help <PACKAGE>"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sapm__help)
            opts="find info install list uninstall update help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sapm__help__find)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sapm__help__help)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
